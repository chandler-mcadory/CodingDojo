function greet(name, time){
    if(name == "Count Dooku"){
        console.log("I'm coming for you, Dooku!")
    }
    else if(name == "Anakin"){
        console.log("Good " + time + " Anakin!")
    }
    else {
        console.log("Good " + time + " " + name + "!")
    }
}


greet("Meg", "Morning")